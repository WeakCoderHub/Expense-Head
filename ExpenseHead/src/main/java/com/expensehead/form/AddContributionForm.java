package com.expensehead.form;

public class AddContributionForm {
	
	int contributionAmount;
	int monthContributed;
	String contributionDescription;
	
	public int getContributionAmount() {
		return contributionAmount;
	}
	public void setContributionAmount(int contributionAmount) {
		this.contributionAmount = contributionAmount;
	}
	public int getMonthContributed() {
		return monthContributed;
	}
	public void setMonthContributed(int monthContributed) {
		this.monthContributed = monthContributed;
	}
	public String getContributionDescription() {
		return contributionDescription;
	}
	public void setContributionDescription(String contributionDescription) {
		this.contributionDescription = contributionDescription;
	}
	
	
	

}
